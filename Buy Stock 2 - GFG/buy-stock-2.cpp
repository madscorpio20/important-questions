class Solution{
    public:
    long long maximumProfit(vector<long long>&prices, int n) {
        // Code here
        long long profit = 0;
        long long buy = 1e9;
        for(auto it : prices)
        {
            buy = min(buy,it);
            if(it > buy)
            {
                profit += it-buy;
                buy = it;
            }
        }
        return profit;
    }
};

